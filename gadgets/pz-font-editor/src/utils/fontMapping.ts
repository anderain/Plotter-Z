const unicodeToByte = new Map<number, number>()

for (const [byte, u] of BYTE_ENTRIES) {
  if (u !== null) {
    unicodeToByte.set(u, byte)
  }
}

export function mapUnicodeToBytes(ch: string): { bytes: number[]; error: string | null } {
  if (ch.length === 0) return { bytes: [], error: null }

  const charCode = ch.codePointAt(0)!
  const result: number[] = []

  const direct = unicodeToByte.get(charCode)
  if (direct !== undefined) {
    result.push(direct)
    return { bytes: result, error: null }
  }

  if (charCode >= 0x20 && charCode <= 0x7E) {
    result.push(charCode)
    return { bytes: result, error: null }
  }

  const voicedBase = KATAKANA_VOICED[charCode]
  if (voicedBase !== undefined) {
    const baseByte = unicodeToByte.get(voicedBase)
    if (baseByte !== undefined) {
      result.push(baseByte, DAKUTEN_BYTE)
      return { bytes: result, error: null }
    }
  }

  const halfVoicedBase = KATAKANA_HALFVOICED[charCode]
  if (halfVoicedBase !== undefined) {
    const baseByte = unicodeToByte.get(halfVoicedBase)
    if (baseByte !== undefined) {
      result.push(baseByte, HANDAKUTEN_BYTE)
      return { bytes: result, error: null }
    }
  }

  return { bytes: [], error: `No mapping for '${ch}' (U+${charCode.toString(16).toUpperCase()})` }
}

